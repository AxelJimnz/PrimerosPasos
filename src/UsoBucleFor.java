public class UsoBucleFor {
    /**
     * @param args
     */
    public static void main(String[] args) {
        
        int numero=2;
        int multi=0;
        for(int i=0; i<10; i++){
            multi= i * numero;            
            System.out.println(i+" X " +numero+ "=" +multi);
        }
    }
}
